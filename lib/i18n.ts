export const t = (key: string, lang: Language): string => {
  const keys = key.split(".");
  let current: any = translations[lang];
  for (const k of keys) {
    if (current && current[k] !== undefined) {
      current = current[k];
    } else {
      return key;
    }
  }
  return current as string;
};
